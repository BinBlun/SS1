day = int(input("Enter a number 1-7 : "))

if (day == 1):
    print(day, " is Monday")
elif (day == 2):
    print(day, " is Tuesday")
elif (day == 3):
    print(day, " is Wednesday")
elif (day == 4):
    print(day, " is Thursday")
elif (day == 5):
    print(day, " is Friday")
elif (day == 6):
    print(day, " is Saturday")
elif (day == 7):
    print(day, " is Sunday")
else:
    print("Wrong Input!!!!!")
